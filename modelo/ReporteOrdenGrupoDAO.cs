        public Mensaje saveDeleteReporteOrdenGrupo(List<ReporteOrdenGrupo> entitysCambios, object[] eliminados, DBContextAdapter dbContext)
        {
            listFuenteDatos = new List<ReporteOrdenGrupo>();
            bool exitoRegistro = true;

            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                if (eliminados != null && eliminados.Length > 0)
                {
                    exitoRegistro = deleteListQuery(eliminados, dbContext);
                }

                entitysCambios = (entitysCambios == null ? new List<ReporteOrdenGrupo>() : entitysCambios);
                if (exitoRegistro && entitysCambios.Count > 0)
                {
                    for (int i = 0; i < entitysCambios.Count; i++)
                    {
                        getSession().Set<ReporteOrdenGrupo>().AddOrUpdate(entitysCambios[i]);
                        listFuenteDatos.Add(entitysCambios[i]);
                        getSession().SaveChanges();
                        //flush();
                        //getSession().evict(entitysCambios.get(i));
                    }
                }
                if (exitoRegistro)
                {
                    getSession().SaveChanges();
                    mensajeResultado.resultado = listFuenteDatos;
                    mensajeResultado.noError = 0;
                    mensajeResultado.error = "";
                    getSession().Database.CurrentTransaction.Commit();
                }
                else
                {
                    getSession().Database.CurrentTransaction.Rollback();
                }

            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("saveDeleteReporteOrdenGrupo()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
            return mensajeResultado;
        }
