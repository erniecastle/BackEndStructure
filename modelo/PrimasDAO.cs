        public Mensaje SavePrimas(List<Primas> agrega, List<Primas> elimina, RegistroPatronal r, DBContextAdapter dbContext)
        {
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                foreach (Primas p in elimina)
                {
                    getSession().Set<Primas>().Attach(p);
                    getSession().Set<Primas>().Remove(p);
                    getSession().SaveChanges();
                }

                for (int i = 0; i < agrega.Count; i++)
                {
                    agrega[i].registroPatronal = r;
                    getSession().Set<Primas>().Add(agrega[i]);
                    getSession().SaveChanges();
                }

                getSession().SaveChanges();
                mensajeResultado.resultado = true;
                mensajeResultado.noError = 0;
                mensajeResultado.error = "";
                getSession().Database.CurrentTransaction.Commit();
