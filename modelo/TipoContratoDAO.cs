        public Mensaje saveDeleteTipoContrato(List<TipoContrato> entitysCambios, object[] clavesDelete, int rango, DBContextAdapter dbContext)
        {
            List<TipoContrato> listEsp = new List<TipoContrato>();
            try
            {
                commit = true;
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                if (clavesDelete != null)
                {
                    commit = deleteListTipoContrato("TipoContrato", new CamposWhere("TipoContrato.clave", clavesDelete, OperadorComparacion.IN, OperadorLogico.AND), dbContext);

                    //deleteListTipoContrato("TipoContrato", "Clave", clavesDelete);
                }
                entitysCambios = (entitysCambios == null ? new List<TipoContrato>() : entitysCambios);
                if (commit && entitysCambios.Count>0)
                {
                    listEsp = agregarListaTipoContrato(entitysCambios, rango);
                }
                if (commit)
                {
                    mensajeResultado.resultado = listEsp;
                    mensajeResultado.noError = 0;
                    mensajeResultado.error = "";
                    getSession().Database.CurrentTransaction.Commit();
                }
                else
                {
                    getSession().Database.CurrentTransaction.Rollback();
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("saveDeleteTipoContrato()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
            return mensajeResultado;
        }
        private List<TipoContrato> agregarListaTipoContrato(List<TipoContrato> entitys, int rango)
        {
            List<TipoContrato> listEsp = new List<TipoContrato>();
            commit = true;
            try
            {
                int i;
                for (i = 0; i < entitys.Count; i++)
                {
                    if (entitys[i].id==0)
                    {
                        listEsp.Add(getSession().Set<TipoContrato>().Add(entitys[i]));
                    }
                    else
                    {
                        getSession().Set<TipoContrato>().Add(entitys[i]);
                    }
                    if (i % rango == 0 & i > 0)
                    {
                        getSession().SaveChanges();
                    }
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("deleteListTipoContrato()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                commit = false;
            }
            return listEsp;
        }
