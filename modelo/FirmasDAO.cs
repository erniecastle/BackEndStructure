        public Mensaje getFirmasPorFechaYRazonSocial(string razonSocial, DateTime fecha, DBContextAdapter dbContext)
        {
            listaFirmas = new List<Firmas>();
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                listaFirmas = (from u in getSession().Set<Firmas>()
                               where u.fecha == (
                               from o in getSession().Set<Firmas>()
                               where o.fecha <= fecha &&
                               o.razonesSociales.clave.Equals(razonSocial) &&
                               o.clave.Equals(u.clave)
                               select new { o.fecha }).Max(p => p.fecha)
                               orderby u.clave
                               select u).ToList();
                mensajeResultado.resultado = listaFirmas;
                mensajeResultado.noError = 0;
                mensajeResultado.error = "";
                getSession().Database.CurrentTransaction.Commit();
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("FirmasPorFechaYRazonSocial()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
            return mensajeResultado;
        }

        public Mensaje saveDeleteFirmas(List<Firmas> entitysCambios, object[] clavesDelete, int rango, DBContextAdapter dbContext)
        {
            listaFirmas = new List<Firmas>();
            try
            {
                commit = true;
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                if (clavesDelete != null)
                {
                    commit = deleteListFirmas(typeof(Firmas).Name, new CamposWhere("Firmas.id", clavesDelete, OperadorComparacion.IN, OperadorLogico.AND), dbContext);
                    //deleteListFirmas("Firmas", "Clave", clavesDelete, "");
                }
                entitysCambios = (entitysCambios == null ? new List<Firmas>() : entitysCambios);
                if (commit && entitysCambios.Count() > 0)
                {
                    listaFirmas = agregarListaFirmas(entitysCambios, rango, dbContext);
                }
                if (commit)
                {
                    mensajeResultado.resultado = listaFirmas;
                    mensajeResultado.noError = 0;
                    mensajeResultado.error = "";
                    getSession().Database.CurrentTransaction.Commit();
                }
                else
                {
                    getSession().Database.CurrentTransaction.Rollback();
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("saveDeleteCFDIRecibo()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
            return mensajeResultado;
        }
        private List<Firmas> agregarListaFirmas(List<Firmas> entitys, int rango, DBContextAdapter dbContext)
        {
            listaFirmas = new List<Firmas>();
            commit = true;
            try
            {
                int i = 0;
                for (i = 0; i < entitys.Count(); i++)
                {
                    if (entitys[i].id > 0)
                    {
                        listaFirmas.Add(getSession().Set<Firmas>().Add(entitys[i]));
                    }
                    else
                    {
                        getSession().Set<Firmas>().AddOrUpdate(entitys[i]);
                    }
                    if (i % rango == 0 & i > 0)
                    {
                        getSession().SaveChanges();
                    }
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("agregarListaFirmas()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
            return listaFirmas;
        }
