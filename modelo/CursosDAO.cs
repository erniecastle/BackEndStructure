        private List<Cursos> agregarListaCursos(List<Cursos> entitys, int rango, DBContextAdapter dbContext)
        {
            List<Cursos> listcur = new List<Cursos>();
            try
            {
                commit = true;
                int i;
                for (i = 0; i < entitys.Count(); i++)
                {
                    if (entitys[i].id == 0)
                    {
                        listcur.Add(getSession().Set<Cursos>().Add(entitys[i]));
                    }
                    else
                    {
                        getSession().Set<Cursos>().AddOrUpdate(entitys[i]);
                    }
                    if (i % rango == 0 & i > 0)
                    {
                        getSession().SaveChanges();
                    }
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("agregarListaCursos()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
            return listcur;
        }
