            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                plaza = (from p in getSession().Set<Plazas>()
                         where p.clave == clave && p.razonesSociales.clave == razonSocial
