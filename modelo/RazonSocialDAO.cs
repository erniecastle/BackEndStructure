        public Mensaje getPorClaveRazonSocial(string claves, DBContextAdapter dbContext)
        {
            RazonSocial razonSocial;
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                razonSocial = (from p in getSession().Set<RazonSocial>()
                               where p.claveRazonSocial == claves
                               select p).SingleOrDefault();
                mensajeResultado.resultado = razonSocial;
                mensajeResultado.noError = 0;
                mensajeResultado.error = "";
                getSession().Database.CurrentTransaction.Commit();

            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("getRazonesPorClave()1_Error: ").Append(ex));
