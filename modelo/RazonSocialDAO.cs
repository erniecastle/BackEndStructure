        public Mensaje getPorIdRazonSocial(decimal? id, DBContextAdapter dbContext)
        {
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                var razonSocial = (from rs in getSession().Set<RazonSocial>()
                                   where rs.id == id
