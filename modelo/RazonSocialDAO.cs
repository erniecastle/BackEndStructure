                                   {
                                       rs.claveRazonSocial,
                                       rs.id,
                                       rs.nombreRazonSocial
                                   }).SingleOrDefault();
                mensajeResultado.resultado = razonSocial;
                mensajeResultado.noError = 0;
                mensajeResultado.error = "";
                getSession().Database.CurrentTransaction.Commit();
