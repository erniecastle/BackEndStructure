        private List<FiniqLiquidCncNom> agregarListaFiniqLiquidCncNom(List<FiniqLiquidCncNom> entitys, int rango, DBContextAdapter dbContext)
        {
            commit = true;
            listaFiniqLiquidCncNom = new List<FiniqLiquidCncNom>();
            try
            {
                int i = 0;
                for (i = 0; i < entitys.Count(); i++)
                {
                    if (entitys[i].id > 0)
                    {
                        listaFiniqLiquidCncNom.Add(getSession().Set<FiniqLiquidCncNom>().Add(entitys[i]));
                    }
                    else
                    {
                        getSession().Set<FiniqLiquidCncNom>().AddOrUpdate(entitys[i]);
                    }
                    if (i % rango == 0 & i > 0)
                    {
                        getSession().SaveChanges();
                    }
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("agregarListaFiniqLiquidCncNom()1_Error: ").Append(ex));
