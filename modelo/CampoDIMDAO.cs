        public Mensaje getPorClaveCampoDIM(string clave, DBContextAdapter dbContext)
        {
            CampoDIM campoDIM = new CampoDIM();
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                campoDIM = (from c in getSession().Set<CampoDIM>()
