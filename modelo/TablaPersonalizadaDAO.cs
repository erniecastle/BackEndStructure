        public Mensaje getPorIdTablaPersonalizada(decimal? id, DBContextAdapter dbContext)
        {
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                var tablaPersonalizadas = (from tp in getSession().Set<TablaPersonalizada>()
                                           where tp.id == id
                                           select new
                                           {
                                               tp.clave,
                                               tp.descripcion,
                                               tp.descripcionAbreviada,
                                               tp.fileXml,
                                               tp.id,
                                               tp.renglonSeleccionado,
