        }

        public Mensaje getTipoElemento(string nombre, DBContextAdapter dbContext)
        {
            TipoElemento tipoElementos;
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                tipoElementos = (from te in getSession().Set<TipoElemento>()
                                 where te.nombre==nombre
                                 select te).SingleOrDefault();
                mensajeResultado.resultado = tipoElementos;
                mensajeResultado.noError = 0;
                mensajeResultado.error = "";
                getSession().Database.CurrentTransaction.Commit();

            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("getTipoElemento()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
            return mensajeResultado;
        }

        public Mensaje getAllTipoElemento(DBContextAdapter dbContext)
        {
            List<TipoElemento> tipoElementos = new List<TipoElemento>();
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                tipoElementos = (from te in getSession().Set<TipoElemento>() select te).ToList();
                mensajeResultado.resultado = tipoElementos;
                mensajeResultado.noError = 0;
                mensajeResultado.error = "";
                getSession().Database.CurrentTransaction.Commit();

