        public Mensaje getSalariosIntegradosDetAll(RazonesSociales razonesSociales, DBContextAdapter dbContext)
        {
            List<SalariosIntegradosDet> salariosIntegradosDet = new List<SalariosIntegradosDet>();
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                salariosIntegradosDet = (from s in getSession().Set<SalariosIntegradosDet>()
                                         join sdi in getSession().Set<SalariosIntegrados>() on
                                         s.salariosIntegrados.id equals sdi.id
                                         join em in getSession().Set<Empleados>() on
                                         sdi.empleados.id equals em.id
                                         where em.razonesSociales.id == razonesSociales.id
                                         orderby s.id
                                         select s).ToList();

                mensajeResultado.resultado = salariosIntegradosDet;
                mensajeResultado.noError = 0;
                mensajeResultado.error = "";
                getSession().Database.CurrentTransaction.Commit();

            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("getSalariosIntegradosDetAll()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
            return mensajeResultado;
        }

