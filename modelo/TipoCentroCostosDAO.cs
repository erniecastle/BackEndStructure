                getSession().Database.CurrentTransaction.Rollback();
            }
            return mensajeResultado;
        }
        private List<TipoCentroCostos> agregarListaTipoCentroCostos(List<TipoCentroCostos> entitys, int rango)
        {
            listEsp.Clear();
            commit = true;
            try
            {
                int i;
                for (i = 0; i < entitys.Count; i++)
                {
                    if (entitys[i].id == 0)
                    {
                        listEsp.Add(getSession().Set<TipoCentroCostos>().Add(entitys[i]));
                    }
                    else
                    {
                        getSession().Set<TipoCentroCostos>().Add(entitys[i]);
                    }
                    if (i % rango == 0 & i > 0)
                    {
                        getSession().SaveChanges();
                    }
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("agregarListaTipoCentroCostos()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                commit = false;
            }
            return listEsp;
        }

