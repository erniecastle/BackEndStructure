        public Mensaje saveDeleteTipoCentroCostos(List<TipoCentroCostos> entitysCambios, object[] clavesDelete, int rango, DBContextAdapter dbContext)
        {
            listEsp = new List<TipoCentroCostos>();
            try
            {
                commit = true;
                inicializaVariableMensaje();
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                if (clavesDelete != null)
                {
                    commit = deleteListQuerys("TipoCentroCostos", new CamposWhere("TipoCentroCostos.clave", clavesDelete, OperadorComparacion.IN, OperadorLogico.AND), dbContext);

                    //deleteListQuerys("TipoCentroCostos", "Clave", clavesDelete);

                }
                entitysCambios = (entitysCambios == null ? new List<TipoCentroCostos>() : entitysCambios);
                if (commit && entitysCambios.Count > 0)
                {
                    listEsp = agregarListaTipoCentroCostos(entitysCambios, rango);
                    if (commit)
                    {
                        getSession().SaveChanges();
                    }
                }
                if (commit)
                {
                    getSession().SaveChanges();
                    mensajeResultado.resultado = listEsp;
                    mensajeResultado.noError = 0;
                    mensajeResultado.error = "";
                    getSession().Database.CurrentTransaction.Commit();
