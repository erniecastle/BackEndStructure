        public Mensaje getElementoExPorContenedor(Contenedor c, DBContextAdapter dbContext)
        {
            ElementoExterno elementoExterno = new ElementoExterno();
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                elementoExterno = (from e in getSession().Set<ElementoExterno>()
                                   where e.contenedor.id == c.id
                                   select e).SingleOrDefault();
                mensajeResultado.resultado = elementoExterno;
                mensajeResultado.noError = 0;
                mensajeResultado.error = "";
