                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
            return mensajeResultado;
        }

        public Mensaje getElementoExAll(DBContextAdapter dbContext)
        {
            List<ElementoExterno> listaElementoExterno = new List<ElementoExterno>();
