        public Mensaje DeleteElementoExterno(List<ElementoExterno> e, DBContextAdapter dbContext)
        {
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                foreach (ElementoExterno elemex in e)
                {
                    getSession().Set<ElementoExterno>().Attach(elemex);
                    getSession().Set<ElementoExterno>().Remove(elemex);
                    getSession().SaveChanges();
                }

                mensajeResultado.resultado = true;
                mensajeResultado.noError = 0;
                mensajeResultado.error = "";
                getSession().Database.CurrentTransaction.Commit();
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("DeleteElementoExterno()1_Error: ").Append(ex));
