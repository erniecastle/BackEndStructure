        public Mensaje saveDeleteTiposVacaciones(List<TiposVacaciones> entitysCambios, object[] clavesDelete, int rango, DBContextAdapter dbContext)
        {
            listTiposVaca = new List<TiposVacaciones>();
            try
            {
                commit = true;
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                if (clavesDelete != null)
                {
                    commit = deleteListQuerys("TiposVacaciones", new CamposWhere("TiposVacaciones.clave", clavesDelete, OperadorComparacion.IN, OperadorLogico.AND), dbContext);
                    //deleteListQuerys("TiposVacaciones", "Clave", clavesDelete);
                }
                entitysCambios = (entitysCambios == null ? new List<TiposVacaciones>() : entitysCambios);
                if (commit && entitysCambios.Count > 0)
                {
                    listTiposVaca = agregarListTiposVacaciones(entitysCambios, rango);
                }
                if (commit)
                {
                    mensajeResultado.resultado = listTiposVaca;
                    mensajeResultado.noError = 0;
                    mensajeResultado.error = "";
                    getSession().Database.CurrentTransaction.Commit();
                }
                else
                {
                    getSession().Database.CurrentTransaction.Rollback();
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("saveDeleteTiposVacaciones()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
                return mensajeResultado;
        }
        private List<TiposVacaciones> agregarListTiposVacaciones(List<TiposVacaciones> entitys, int rango)
        {
            listTiposVaca.Clear();
            commit = true;
            try
            {
                int i;
                for (i = 0; i < entitys.Count; i++)
                {
                    if (entitys[i].id == 0)
                    {
                        listTiposVaca.Add(getSession().Set<TiposVacaciones>().Add(entitys[i]));
                    }
                    else
                    {
                        getSession().Set<TiposVacaciones>().Add(entitys[i]);
                    }
                    if (i % rango == 0 & i > 0)
                    {
                        getSession().SaveChanges();
                    }
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("agregarListTiposVacaciones()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                commit = false;
            }
            return listTiposVaca;
        }
