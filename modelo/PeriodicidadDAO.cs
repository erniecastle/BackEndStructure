        public Mensaje saveDeletePeriodicidad(List<Periodicidad> entitysCambios, object[] clavesDelete, int rango, DBContextAdapter dbContext)
        {
            List<Periodicidad> listEsp = new List<Periodicidad>();
            try
            {
                commit = true;
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                if (clavesDelete != null)
                {
                    commit = deleteListPeriodicidad("Periodicidad", new CamposWhere("Periodicidad.clave", clavesDelete, OperadorComparacion.IN, OperadorLogico.AND), dbContext);
                    //deleteListPeriodicidad("Periodicidad", "Clave", clavesDelete);

                }
                entitysCambios = (entitysCambios == null ? new List<Periodicidad>() : entitysCambios);
                if (commit && entitysCambios.Count > 0)
                {
                    listEsp = agregarListaPeriodicidad(entitysCambios, rango);
                    if (commit)
                    {
                        getSession().SaveChanges();
                    }
                }
                if (commit)
                {
                    mensajeResultado.resultado = listEsp;
                    mensajeResultado.noError = 0;
                    mensajeResultado.error = "";
                    getSession().Database.CurrentTransaction.Commit();
                }
                else
                {
                    getSession().Database.CurrentTransaction.Rollback();
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("getPeriodicidadPorClave()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
            return mensajeResultado;
        }
        public List<Periodicidad> agregarListaPeriodicidad(List<Periodicidad> entitys, int rango)
        {
            List<Periodicidad> listEsp = new List<Periodicidad>();
            try
            {
                int i;
                commit = true;
                for (i = 0; i < entitys.Count; i++)
                {
                    if (entitys[i].id == 0)
                    {
                        listEsp.Add(getSession().Set<Periodicidad>().Add(entitys[i]));
                    }
                    else
                    {
                        getSession().Set<Periodicidad>().Add(entitys[i]);
                    }
                    if (i % rango == 0 & i > 0)
                    {
                        getSession().SaveChanges();
                    }
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("agregarListaPeriodicidad()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
                commit = false;
            }
            return listEsp;
        }
