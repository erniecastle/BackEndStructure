        public Mensaje getConfiguraTimbradoPrincipal(ConfiguraTimbrado configuraTimbrado, DBContextAdapter dbContext)
        {
            ConfiguraTimbrado confTimbrado = new ConfiguraTimbrado();
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                var query = (from ct in getSession().Set<ConfiguraTimbrado>()
                             where ct.principal == true                          
                             select ct);
                if (configuraTimbrado == null ? false : configuraTimbrado.id > 0)
                {
                    query = (from subquery in query
                             where subquery.id == configuraTimbrado.id
                             select subquery);
                }

                confTimbrado = query.SingleOrDefault();
                mensajeResultado.resultado = confTimbrado;
                mensajeResultado.noError = 0;
                mensajeResultado.error = "";
                getSession().Database.CurrentTransaction.Commit();
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("ConfiguraTimbradoPrincipal()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
            return mensajeResultado;
        }

        public Mensaje saveConfiguraTimbrado(List<ConfiguraTimbrado> entitysCambios, DBContextAdapter dbContext)
        {
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                int i = 0;
                for(i=0; i < entitysCambios.Count(); i++)
                {
                    getSession().Set<ConfiguraTimbrado>().Add(entitysCambios[i]);
                    getSession().SaveChanges();
                }
                mensajeResultado.resultado = entitysCambios;
                mensajeResultado.noError = 0;
                mensajeResultado.error = "";
                getSession().Database.CurrentTransaction.Commit();
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("saveConfiguraTimbrado()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
            return mensajeResultado;
        }

        public Mensaje getPorIdConfiguraTimbrado(decimal? id, DBContextAdapter dbContext)
        {
            try
            {
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                var listConfTimbradoAll = (from ct in getSession().Set<ConfiguraTimbrado>()
                                           where ct.id==id
                                       select new {
                                           ct.clave,
                                           ct.contraseña,
                                           ct.descripcion,
                                           ct.id,
                                           ct.principal,
                                           ct.URL,
                                           ct.usuario
                                       }).SingleOrDefault();
                mensajeResultado.resultado = listConfTimbradoAll;
                mensajeResultado.noError = 0;
                mensajeResultado.error = "";
                getSession().Database.CurrentTransaction.Commit();
            }
            catch (Exception ex)
            {

                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("getPorIdConfiguraTimbrado()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }

            return mensajeResultado;
        }
    }
