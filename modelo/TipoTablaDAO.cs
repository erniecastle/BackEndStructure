        public Mensaje saveDeleteTipoTabla(List<TipoTabla> entitysCambios, object[] clavesDelete, int rango, DBContextAdapter dbContext)
        {
            listEsp = new List<TipoTabla>();
            try
            {
                commit = true;
                inicializaVariableMensaje();
                setSession(dbContext.context);
                getSession().Database.BeginTransaction();
                if (clavesDelete != null)
                {
                    commit = deleteListQuerys("TipoTabla", new CamposWhere("TipoTabla.clave", clavesDelete, OperadorComparacion.IN, OperadorLogico.AND), dbContext);
                    //deleteListQuerys("TipoTabla", "Clave", clavesDelete);
                }
                entitysCambios = (entitysCambios == null ? new List<TipoTabla>() : entitysCambios);
                if (commit && entitysCambios.Count>0)
                {
                    listEsp = agregarListaTipoTabla(entitysCambios, rango);
                }
                if (commit)
                {
                    mensajeResultado.resultado = listEsp;
                    mensajeResultado.noError = 0;
                    mensajeResultado.error = "";
                    getSession().Database.CurrentTransaction.Commit();
                }
                else
                {
                    getSession().Database.CurrentTransaction.Rollback();
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("getTipoTablaPorClave()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                getSession().Database.CurrentTransaction.Rollback();
            }
            return mensajeResultado;
        }
        private List<TipoTabla> agregarListaTipoTabla(List<TipoTabla> entitys, int rango)
        {
            listEsp.Clear();
            commit = true;
            try
            {
                int i;
                for (i = 0; i < entitys.Count; i++)
                {
                    if (entitys[i].id== 0)
                    {
                        listEsp.Add(getSession().Set<TipoTabla>().Add(entitys[i]));
                    }
                    else
                    {
                        getSession().Set<TipoTabla>().Add(entitys[i]);
                    }
                    if (i % rango == 0 & i > 0)
                    {
                        getSession().SaveChanges();
                    }
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine(concatena.Remove(0, concatena.Length).Append(msgError).Append("agregarListaTipoTabla()1_Error: ").Append(ex));
                mensajeResultado.noError = ControlErroresEntity.buscaNoErrorPorExcepcion(ex);
                mensajeResultado.error = ex.GetBaseException().ToString();
                mensajeResultado.resultado = null;
                commit = false;
            }
            return listEsp;
        }
